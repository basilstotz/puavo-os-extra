PFAD="./patch/final"
if test -d $PFAD/lists.d;then
    LISTS="$(ls $PFAD/lists.d|grep .list|grep -v off|xargs)"
    FIRST=0
    if test -n "$LISTS";then
	for L in $LISTS;do
	    for D in $(cat $PFAD/lists.d/$L|xargs); do
		if test $FIRST = 0; then
		    DEPS="$D"
		    FIRST=1
		else
		    DEPS="$DEPS, $D"
		fi
	    done
	done
    fi
fi

